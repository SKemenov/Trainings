extension Double {
    static let almostInvisibleButTouchable = 0.011
    static let oneDayDuration: Double = 60 * 60 * 24
    static let oneWeekDuration: Double = 60 * 60 * 24 * 7
    static let fullTwoWeeksDuration: Double = 60 * 60 * 24 * (7 + 6)
    static let fullOneWeekDuration: Double = 60 * 60 * 24 * 6
}
